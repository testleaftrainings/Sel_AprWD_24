package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
		
		int num=5;

		for (int i = 0; i <num ; i++) {
			
			System.out.println(i);
		}
		
		System.out.println("  ");
		
		for (int i = num; i >=0; i--) {
			System.out.println(i);
		
		}

	}

}
